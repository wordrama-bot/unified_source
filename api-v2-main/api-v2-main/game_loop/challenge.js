// Assume supabase is already initialized
const { createClient } = require('@supabase/supabase-js');

const supabaseUrl = 'https://qflfxxbnhwaxkxsygjqu.supabase.co';
const supabaseKey = '';
const supabase = createClient(supabaseUrl, supabaseKey);

async function ensureChallengeProgress(players, challengeId) {
  for (const player of players) {
    const { data, error } = await supabase
      .from('_challenge_progress')
      .select('id')
      .eq('player_id', player.id)
      .eq('challenge_id', challengeId)
      .single();

    if (!data) {
      // No record found, create a new one
      const { error: insertError } = await supabase
        .from('_challenge_progress')
        .insert([
          {
            player_id: player.id,
            challenge_id: challengeId,
            status: 'UNLOCKED',
          },
        ]);

      if (insertError) {
        console.error(
          `Error creating progress for player ${player.id}:`,
          insertError,
        );
      } else {
        console.log(`Created challenge progress for player ${player.id}`);
      }
    } else {
      console.log(`Record already exists for player ${player.id}`);
    }
  }
}

